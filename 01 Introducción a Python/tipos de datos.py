# Valores numéricos
print(type(150)) # <class int> -> int -> integer -> Número entero
print(type(276.24)) # float -> Números reales (con decimales)
print(type(12j)) # complex -> Número complejo
# Valores lógicos
print(type(True)) # bool -> booleando -> Verdadero / Falso
print(type(False))
# Secuencias
print(type("Hola mundo")) # str -> string -> Texto
print(type([1,2,6,"Hola",True])) # list -> conjunto ordenado y mutable de datos
print(type((1,2,6,"Hola",True))) # tuple -> conjunto ordenado e inmutable de datos
print(type({"llave": 123, "otra llave": 56})) # dict -> diccionario -> conjunto ordenado, nombrado y mutable de datos